            <div id="short">The Kandy Esala Perahera <i> (the Sri Dalada Perahara procession of Kandy)</i><br><br>
            also known as The <b> Festival of the Tooth </b> is a festival held in July and <ins> August in Kandy, Sri Lanka.</ins><br><br> 
            This historical procession isheld annually to pay homage to the Sacred Tooth Relic of Buddha housed at the SriDalada Maligawa in Kandy.<br>
            A unique symbol of Sri Lanka, the procession consists of traditional localdance such as fire dances and performances in whip-dance garments.<br>
            The festival ends with the traditional Diya-kepeema ritual,a water cutting ceremony which is held at the Mahaweli River at Getambe, Kandy.[1]
            </div><br><br>

