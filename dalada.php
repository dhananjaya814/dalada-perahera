        <h2><ins>History</ins></h2>

            <div id="history">The Esala is believed[who?] to be a fusion of two separate but interconnected "Peraheras" (Processions) – The Esala and Dalada. <br>
            The Esala Perahera, which is thought to date back to the 3rd century BC, was a ritual enacted to request the gods for rainfall.<br>
            The Dalada Perahera is believed to have begun when the Sacred Tooth Relic of Lord Buddha was brought to Sri Lanka from India during the 4th century CE, eight hundred years after the passing away of the Buddha.[citation needed]
            </div>

