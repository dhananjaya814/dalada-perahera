        <h3><ins>The Procession</ins></h3>

              <div id="procession">The Kandy Esala Perahera begins with Kap Situveema or Kappa, in which a sanctified young jackfruit tree (Artocarpus integrifolia)<br>
              is cut and planted on the premises of each of the four Devales dedicated to the <br>
              four guardian gods <b><i> Natha, Vishnu, Katharagama and the goddess Pattini.</i></b><br>
              Traditionally it was meant to shower blessing on the King and the people.[citation needed]</div>

